class Z4 
{
	public static void main(String[] args) 
	{
		boolean b1 = true, b2 = false;
		System.out.println(b1 || b2);
		System.out.println(b1 && b2);
		System.out.println(b1 || !b2);
		System.out.println(!b1 && b2);
		System.out.println(!b1 || b2);
		System.out.println(b1 && !b2);
		System.out.println(b1 + ", " +  b2);
	}
}
